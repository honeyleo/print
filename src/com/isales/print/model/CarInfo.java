package com.isales.print.model;

public class CarInfo {

	private String cartype = "";//意向车型
	private String carrequire = "";//用车需求
	private String cardate = "";//计划买车时间
	private String allprice = "";//全包价
	private String guideprice = "";//指导价
	private String noprice = "";//净车价
	private String safeprice = "";//保险
	private String duty = "";//购置税
	private String cost = "";//上牌费
	public String getCartype() {
		return cartype;
	}
	public void setCartype(String cartype) {
		this.cartype = cartype;
	}
	public String getCarrequire() {
		return carrequire;
	}
	public void setCarrequire(String carrequire) {
		this.carrequire = carrequire;
	}
	public String getCardate() {
		return cardate;
	}
	public void setCardate(String cardate) {
		this.cardate = cardate;
	}
	public String getAllprice() {
		return allprice;
	}
	public void setAllprice(String allprice) {
		this.allprice = allprice;
	}
	public String getGuideprice() {
		return guideprice;
	}
	public void setGuideprice(String guideprice) {
		this.guideprice = guideprice;
	}
	public String getNoprice() {
		return noprice;
	}
	public void setNoprice(String noprice) {
		this.noprice = noprice;
	}
	public String getSafeprice() {
		return safeprice;
	}
	public void setSafeprice(String safeprice) {
		this.safeprice = safeprice;
	}
	public String getDuty() {
		return duty;
	}
	public void setDuty(String duty) {
		this.duty = duty;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	
}
