package com.isales.print.model;

public class Mortgage {

	private String a_money = "";//贷款金额
	private String a_date = "";//贷款时间
	private String b_money = "";//首付金额
	private String c_money = "";//月供金额
	public String getA_money() {
		return a_money;
	}
	public void setA_money(String a_money) {
		this.a_money = a_money;
	}
	public String getA_date() {
		return a_date;
	}
	public void setA_date(String a_date) {
		this.a_date = a_date;
	}
	public String getB_money() {
		return b_money;
	}
	public void setB_money(String b_money) {
		this.b_money = b_money;
	}
	public String getC_money() {
		return c_money;
	}
	public void setC_money(String c_money) {
		this.c_money = c_money;
	}
	
}
